package Day2;

public class OpPredenceDemo {

	public static void main(String[] args) {

		int result = 10 + 5 * 2;
		System.out.println("10 + 5 * 2 = " + result);
		
		int result2 = (10 + 5) * 2;
		System.out.println("(10 + 5) * 2  =" + result2);
		
		int a = 20 - 5 - 2;
		System.out.println("20 - 5 - 2 = " + a);
		
		int x, y, z;
		x = y = z = 10;
		System.out.println("x = " + x + " , y = " + y + " , z = " + z);
	}

}
